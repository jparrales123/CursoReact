    private void jTableListaEstMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableListaEstMouseClicked
        // TODO add your handling code here:
        int idFila=jTableListaEst.getSelectedRow();
        jNombre1.setText(jTableListaEst.getValueAt(idFila, 1).toString());
        jApellido.setText(jTableListaEst.getValueAt(idFila, 2).toString());
        jNivel.setSelectedItem(jTableListaEst.getValueAt(idFila, 3));
        jTelefono.setText(jTableListaEst.getValueAt(idFila, 4).toString());
        jCorreo.setText(jTableListaEst.getValueAt(idFila, 5).toString());
        jDireccion.setText(jTableListaEst.getValueAt(idFila, 6).toString());
        Image img=((ImageIcon)jTableListaEst.getValueAt(idFila, 7)).getImage().getScaledInstance(jImagenEst.getWidth(),jImagenEst.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon imgEst=new ImageIcon(img);
        jImagenEst.setIcon(imgEst);
    }//GEN-LAST:event_jTableListaEstMouseClicked

