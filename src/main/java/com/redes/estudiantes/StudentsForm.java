    public void listarEstudiantes(){
        EstudiantesQuery query=new EstudiantesQuery();
        ArrayList<Estudiante> estudiantes=query.listarEstudiante(idUserLog);
        ImageIcon img;
        for (int i = 0; i < estudiantes.size(); i++){
            model.addRow(new Object[]{
                estudiantes.get(i).getIdEstudiante(),
                estudiantes.get(i).getNombre(),
                estudiantes.get(i).getApellido(),
                estudiantes.get(i).getNivel(),
                estudiantes.get(i).getTelefono(),
                estudiantes.get(i).getCorreo(),
                estudiantes.get(i).getDireccion(),
                img = new ImageIcon(new ImageIcon(estudiantes.get(i).getImagen()).getImage().getScaledInstance(60,50,Image.SCALE_SMOOTH)),
            });
            
        }
    }
    
