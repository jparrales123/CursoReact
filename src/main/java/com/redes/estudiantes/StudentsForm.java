    private void jAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAgregarActionPerformed
        // TODO add your handling code here:
        String nombre=jNombre1.getText();
        String apellido=jApellido.getText();
        String nivel=jNivel.getSelectedItem().toString();
        int telefono=Integer.parseInt(jTelefono.getText());
        String correo=jCorreo.getText();
        String direccion=jDireccion.getText();
        int idUser=idUserLog;
        try {
            Path pth=Paths.get(imagenPath);
            img=Files.readAllBytes(pth);
            
        } catch (IOException ex) {
            ex.printStackTrace(System.out);
        }
        Estudiante est = new Estudiante(0, nombre, apellido, nivel, telefono,correo,direccion,img,idUserLog);
        EstudiantesQuery query=new EstudiantesQuery();
        query.insetertarEstudiante(est);
    }//GEN-LAST:event_jAgregarActionPerformed

